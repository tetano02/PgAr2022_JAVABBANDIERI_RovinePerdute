package it.unibs.fp.rovinePerdute;

public class Posizione {
	
	int coordinataX;
	int coordinataY;
	int coordinataH;
	
	public int getCoordinataX() {
		return coordinataX;
	}
	public void setCoordinataX(int coordinataX) {
		this.coordinataX = coordinataX;
	}
	public int getCoordinataY() {
		return coordinataY;
	}
	public void setCoordinataY(int coordinataY) {
		this.coordinataY = coordinataY;
	}
	public int getCoordinataH() {
		return coordinataH;
	}
	public void setCoordinataH(int coordinataH) {
		this.coordinataH = coordinataH;
	}

}
